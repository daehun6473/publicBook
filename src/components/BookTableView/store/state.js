/*
====================================================
Vuex State (state.js)
====================================================

역할
- 애플리케이션 전체에서 사용하는 데이터를 저장
- 여러 컴포넌트가 같은 데이터를 공유 가능
- 화면에 표시되는 데이터의 원본(Source of Truth)

----------------------------------------------------
현재 State 구조

books
- 도서 목록 저장
- API 조회 결과를 저장

예시
[
  {
    bookNo: 1,
    bookName: 'Java'
  },
  {
    bookNo: 2,
    bookName: 'Spring'
  }
]

----------------------------------------------------

totalCount
- 전체 도서 개수 저장
- 페이징 처리 시 사용

예시

전체 데이터 : 105건
현재 페이지 : 1

totalCount = 105

----------------------------------------------------

loading
- API 통신 중 여부 저장

true
→ 서버 요청 중

false
→ 요청 완료

주로 화면에서 로딩 스피너를 보여줄 때 사용

예시

v-if="loading"

----------------------------------------------------

searchParam
- 검색 조건 저장

pageNo
  현재 페이지 번호

numOfRows
  페이지당 조회 건수

book
  검색할 도서명

예시

searchParam = {
  pageNo: 1,
  numOfRows: 10,
  book: 'Java'
}

----------------------------------------------------

Vuex 데이터 흐름

사용자 검색
      ↓
searchParam 변경
      ↓
Action 실행
      ↓
API 호출
      ↓
books 저장
      ↓
화면 자동 갱신

----------------------------------------------------

주의사항

잘못된 방식

state.books = []

올바른 방식

commit('SET_BOOKS', [])

state는 직접 수정하지 않고
반드시 mutation을 통해 변경한다.

----------------------------------------------------

면접 질문

Q. state란?

A.
Vuex에서 전역으로 관리하는 데이터 저장소이다.

Q. state를 사용하는 이유는?

A.
여러 컴포넌트에서 동일한 데이터를
공유하기 위해 사용한다.

Q. loading 상태를 왜 관리하는가?

A.
API 요청 중 여부를 사용자에게
표시하기 위해 사용한다.
====================================================
*/

export default {
    books: [],
    totalCount: 0,
    loading: false,

    searchParam: {
        pageNo: '',
        numOfRows: '',
        book: ''
    }
}